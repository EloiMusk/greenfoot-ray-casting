package util;

public class Orientation {
    public int x;
    public int y;
    public int z;

    public Orientation(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public Orientation() {
        this.x = 0;
        this.y = 0;
        this.z = 0;
    }
}
